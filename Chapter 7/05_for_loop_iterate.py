## For Loop with Lists :-
l = [12, 56, 34, 78, 90, 34, 786]
for i in l:
    if(i==786):
        print("\n")
    else:
        print(i)


## For Loop with Tuple :-
t = (6, 23, 539, 9032, 110, 00)
for i in t:
    print(i)
else:
    print("\n") 


## For Loop with Strings :-
s = "Shaikh"
for i in s:
    print(i);
    
