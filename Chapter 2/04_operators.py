# Arithmetic Operators

a = 93
b = 7
c = a + b
print(c);

# Assignment Operator

a = 5-2     # Assign 5-2 in a
print(a);

b = 5
# b +=4   # (b = b + 4) Increment the value of b by 4, assign it to b
b -=1   # (b = b - 1) Decrement the value of b by 1, assign it to b
print(b);

# Comparison Operator

p = 5<4
print(p);
q = 5>=5
print(q);
r = 4!=9
print(r);
s = 4==3+1
print(s,"\n");

# Logical Operator

u = True and False
print(u);
v = True or False
print(v);
w = 5!=4 and 2<4
print(w);
x = 8==8  or 3==9
print(x);
z = not(10!=10)     # ~(~p) = p
print(z, "\n");
 
# Truth table of 'or'
print("T or T is ", True or True);
print("T or F is ", True or False);
print("F or T is ", False or True);
print("F or F is ", False or False, "\n");

# Truth table of 'and'
print("T and T is ", True and True);
print("T and F is ", True and False);
print("F and T is ", False and True);
print("F and F is ", False and False, "\n");

# Truth table of 'not'
print("not(T) is", not(True));
print("not(F) is", not(False));





